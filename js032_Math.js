//최대값
console.log(Math.max(1, 2, 3, 4, 5));

//최소값
console.log(Math.min(1, 2, 3, 4, 5));

//절대값
console.log(Math.abs(-10));
//반올림-자릿수 지정할 수없음
console.log(Math.round(25.3333));

//올림
console.log(Math.ceil(23.244));
//내림
console.log(Math.floor(43.23423));

//난수
console.log(Math.random());
// 자바랑 같음 그냥 복습한다는 느낌으로 하면됨

console.log((25.523).toFixed(1));
console.log((25.526).toFixed(1));
